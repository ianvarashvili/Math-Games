def calculate_rank(points: int) -> str:
    """
    ქულების რაოდენობით წოდებას აბრუნებს.

    მაქსიმუმ 1500 ქულა ერთ კლასში:
    (15 თამაში × 100 ქულა)

    0-299    → მათემატიკის მოსწავლე
    300-799  → მათემატიკის რაინდი
    800-1299 → მათემატიკის გმირი
    1300+    → მათემატიკის ლეგენდა
    """

    if points >= 1300:
        return "მათემატიკის ლეგენდა"
    elif points >= 800:
        return "მათემატიკის გმირი"
    elif points >= 300:
        return "მათემატიკის რაინდი"
    else:
        return "მათემატიკის მოსწავლე"